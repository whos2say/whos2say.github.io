#!/usr/bin/env node
/**
 * Generate admin/config.yml for the current Vercel environment.
 *
 * Approach: template + build-time env vars (recommended over duplicate config files).
 * - admin/config.shared.yml — collections (edit this for CMS schema changes)
 * - admin/config.yml — generated output (committed with production defaults for local dev)
 *
 * Vercel runs this on every deploy via vercel.json buildCommand.
 * See docs/STAGING_WORKFLOW.md
 */

import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const sharedPath = path.join(root, 'admin', 'config.shared.yml')
const outputPath = path.join(root, 'admin', 'config.yml')

const PRODUCTION = {
  gitBranch: 'main',
  siteOrigin: 'https://www.whostosay.org',
  siteDomain: 'www.whostosay.org',
  label: 'production',
}

const STAGING = {
  gitBranch: 'staging',
  siteOrigin: 'https://staging.whostosay.org',
  siteDomain: 'staging.whostosay.org',
  label: 'staging',
}

/**
 * Resolve Decap backend target from Vercel / explicit env.
 * @returns {{ gitBranch: string, siteOrigin: string, siteDomain: string, label: string, cmsEnabled: boolean }}
 */
function resolveEnvironment() {
  const vercelEnv = process.env.VERCEL_ENV // production | preview | development
  const gitRef = process.env.VERCEL_GIT_COMMIT_REF || process.env.DECAP_GIT_BRANCH || 'main'

  // Explicit overrides (set per-environment in Vercel dashboard)
  if (process.env.DECAP_GIT_BRANCH && process.env.SITE_ORIGIN) {
    const origin = process.env.SITE_ORIGIN.replace(/\/$/, '')
    return {
      gitBranch: process.env.DECAP_GIT_BRANCH,
      siteOrigin: origin,
      siteDomain: process.env.DECAP_SITE_DOMAIN || new URL(origin).hostname,
      label: process.env.DECAP_ENV_LABEL || process.env.DECAP_GIT_BRANCH,
      cmsEnabled: process.env.DECAP_CMS_ENABLED !== 'false',
    }
  }

  if (vercelEnv === 'production' || gitRef === 'main') {
    return { ...PRODUCTION, cmsEnabled: true }
  }

  if (gitRef === 'staging') {
    return { ...STAGING, cmsEnabled: true }
  }

  // Feature-branch previews: CMS disabled by default (OAuth callback won't match)
  const previewOrigin = process.env.VERCEL_URL
    ? `https://${process.env.VERCEL_URL}`
    : 'http://localhost:3000'

  return {
    gitBranch: gitRef,
    siteOrigin: previewOrigin,
    siteDomain: process.env.VERCEL_URL || 'localhost',
    label: `preview:${gitRef}`,
    cmsEnabled: false,
  }
}

function buildHeader(env) {
  const cmsNote = env.cmsEnabled
    ? `# CMS commits to Git branch: ${env.gitBranch}`
    : '# CMS disabled on this deployment — use /admin/ on www or staging.whostosay.org only'

  return `# GENERATED by scripts/generate-decap-config.mjs — do not edit by hand.
# Regenerate: npm run build:decap-config
# Environment: ${env.label}
# Docs: docs/STAGING_WORKFLOW.md

${cmsNote}

backend:
  name: github
  repo: whos2say/whostosay.github.io
  branch: ${env.gitBranch}
  base_url: ${env.siteOrigin}
  auth_endpoint: api/auth
  site_domain: ${env.siteDomain}

publish_mode: editorial_workflow

media_folder: assets/images/uploads
public_folder: /assets/images/uploads

locale: en

site_url: ${env.siteOrigin}
display_url: ${env.siteOrigin}

`
}

function main() {
  if (!fs.existsSync(sharedPath)) {
    console.error('Missing admin/config.shared.yml — run from repo root.')
    process.exit(1)
  }

  const env = resolveEnvironment()
  const shared = fs.readFileSync(sharedPath, 'utf8').replace(/^\uFEFF/, '')
  const header = buildHeader(env)
  fs.writeFileSync(outputPath, header + shared, 'utf8')

  console.log(`[decap-config] Wrote admin/config.yml`)
  console.log(`  environment: ${env.label}`)
  console.log(`  git branch:  ${env.gitBranch}`)
  console.log(`  site origin: ${env.siteOrigin}`)
  console.log(`  cms enabled: ${env.cmsEnabled}`)
}

main()
